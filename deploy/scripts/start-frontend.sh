#!/bin/bash
# ============================================================
# 前端启动脚本 — 麒麟安全智能运维 Agent
# ============================================================

set -e

echo "=========================================="
echo " 麒麟安全智能运维 Agent — 前端启动"
echo "=========================================="

cd frontend

if [ ! -d "node_modules" ]; then
    echo "安装前端依赖..."
    npm install
fi

echo "启动 Vite 开发服务器..."
echo "提示: 前端页面可访问 http://localhost:5173"
echo ""

npm run dev
